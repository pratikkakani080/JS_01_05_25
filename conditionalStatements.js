// if..else
let color = 'red'
if (color === 'red') {
    console.log('red**');
} else if (color === 'black') {
    console.log('black**');
} else {
    console.log('default**');
}

// switch..case
switch(color) {
    case 'red':
        console.log('color is red');
        break;
    
    case 'black':
        console.log('color is black');
        break;
    
    default:
        console.log('color is default');
        break;
}

// ternary operator